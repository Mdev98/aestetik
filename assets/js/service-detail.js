function loadServiceDetails() {
    const url = window.location.href;
    const decodedUrl = decodeURIComponent(url);
    const serviceName = decodedUrl.split("#")[1];
    
    const service = serviceDetails[serviceName];
    if (!service) {
        window.location.href = "./service.html";
        return;
    }

    document.querySelector(".service-content-title").textContent = service.title;
    document.querySelector(".service-content-text").textContent = service.content;
    
    const serviceItems = document.querySelectorAll(".service-content-item");
    serviceItems.forEach((item, index) => {
        const title = item.querySelector(".service-content-item-title");
        const text = item.querySelector(".service-content-item-text");
        
        if (service.items[index]) {
            title.innerHTML = service.items[index].title;
            text.textContent = service.items[index].content;
        }
    });
}

document.addEventListener("DOMContentLoaded", loadServiceDetails);